package com.innowhite.vo;

import java.io.Serializable;

public class PointVO implements Serializable{
	float x;
	float y;
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
}
